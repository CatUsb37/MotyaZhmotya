function createFlyingImg(src) {
    const img = document.createElement('img');
    img.src = src;
    img.style.position = 'fixed';
    img.style.width = '150px'; 
    img.style.zIndex = '1000';
    img.style.pointerEvents = 'none'; 
    document.body.appendChild(img);


    let posX = Math.random() * (window.innerWidth - 150);
    let posY = Math.random() * (window.innerHeight - 150);
    

    let velX = (Math.random() > 0.5 ? 1 : -1) * (2 + Math.random() * 3);
    let velY = (Math.random() > 0.5 ? 1 : -1) * (2 + Math.random() * 3);

    function animate() {
        const screenWidth = window.innerWidth;
        const screenHeight = window.innerHeight;

        posX += velX;
        posY += velY;

        if (posX + 150 >= screenWidth || posX <= 0) velX = -velX;
        if (posY + 150 >= screenHeight || posY <= 0) velY = -velY;

        img.style.left = posX + 'px';
        img.style.top = posY + 'px';

        requestAnimationFrame(animate);
    }

    animate();
}

